class LibraryMember
  {
    int memberId;
    String fullName;
    int age;
    String mobileNo;
    int issuedBooks;
    LibraryMember(int memberId,String fullName,int age,String mobileNo,int issuedBooks)
    {
      this.memberId=memberId;
      this.fullName=fullName;
      this.age=age;
      this.mobileNo=mobileNo;
      this.issuedBooks=issuedBooks;
    }
  }